const localeOptions: Array<{ value: AppLocale; labelKey: string }> = [
  { value: 'system', labelKey: 'language.system' },
  { value: 'en', labelKey: 'language.en' },
  { value: 'zh-TW', labelKey: 'language.zh-TW' },
  { value: 'zh-CN', labelKey: 'language.zh-CN' },
  { value: 'ja', labelKey: 'language.ja' },
  { value: 'ko', labelKey: 'language.ko' },
  { value: 'es', labelKey: 'language.es' },
]

function resolveLocale(locale: AppLocale, osLanguage = navigator.language): ResolvedLocale {
  const lang = locale === 'system' ? osLanguage : locale
  const normalized = lang.toLowerCase()
  if (normalized.startsWith('zh')) {
    return normalized.includes('cn') || normalized.includes('sg') || normalized.includes('hans') ? 'zh-CN' : 'zh-TW'
  }
  if (normalized.startsWith('ja')) return 'ja'
  if (normalized.startsWith('ko')) return 'ko'
  if (normalized.startsWith('es')) return 'es'
  return 'en'
}

function interpolate(template: string, params?: Record<string, string | number>): string {
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? `{${key}}`))
}

interface I18nContextValue {
  locale: AppLocale
  resolvedLocale: ResolvedLocale
  localeOptions: Array<{ value: AppLocale; label: string }>
  setLocale(locale: AppLocale): Promise<void>
  t(key: string, params?: Record<string, string | number>): string
  severityLabel(severity: BugSeverity): string
}

const I18nContext = createContext<I18nContextValue | null>(null)

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<AppLocale>('system')
  const [ready, setReady] = useState(false)
  const resolvedLocale = resolveLocale(locale)

  useEffect(() => {
    api.settings.get().then(settings => {
      setLocaleState(settings.locale)
      setReady(true)
    }).catch(() => setReady(true))
  }, [])

  const value = useMemo<I18nContextValue>(() => {
    const dict = dictionaries[resolvedLocale]
    const t = (key: string, params?: Record<string, string | number>) => interpolate(dict[key] ?? dictionaries.en[key] ?? key, params)
    return {
      locale,
      resolvedLocale,
      localeOptions: localeOptions.map(option => ({ value: option.value, label: t(option.labelKey) })),
      async setLocale(next) {
        const settings = await api.settings.setLocale(next)
        setLocaleState(settings.locale)
      },
      t,
      severityLabel: (severity) => t(`severity.${severity}`),
    }
  }, [locale, resolvedLocale])

  if (!ready) return null
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) {
    const t = (key: string, params?: Record<string, string | number>) => interpolate(dictionaries.en[key] ?? key, params)
    return {
      locale: 'en' as AppLocale,
      resolvedLocale: 'en' as ResolvedLocale,
      localeOptions: localeOptions.map(option => ({ value: option.value, label: t(option.labelKey) })),
      setLocale: async () => {},
      t,
      severityLabel: (severity: BugSeverity) => t(`severity.${severity}`),
    }
  }
  return ctx
}
